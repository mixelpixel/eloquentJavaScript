// Arrays are collections of data which are organized by the order in which
// data is retrieved: the index, which starts at 0.

var listOfNumbers = [2, 3, 5, 7, 11];
console.log(listOfNumbers[1]); // <--- 3, 2nd index position, 1
console.log(listOfNumbers[1-1]); // <--- 2 1st index position, 0
